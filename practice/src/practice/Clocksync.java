package practice;

import java.util.*;
public class Clocksync {
	public static int C;
	public static Scanner sc = new Scanner(System.in);
	public static void main(String[] args) {
		
		C = sc.nextInt();
		for(int i=0;i<C;i++) {
			Solve();
		}
	}
	private static void Solve() {
		int[] clock = new int[16];
		
		for(int i=0;i<16;i++) {
			clock[i] = sc.nextInt();
		}
		
	}

}
